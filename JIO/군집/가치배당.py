import pandas as pd
import numpy as np
import os
from sklearn.cluster import KMeans
from sklearn.preprocessing import StandardScaler
import warnings

warnings.filterwarnings('ignore')

def run_quant_clustering_from_list(file_list, n_clusters=3, freq='Q'):
    price_data = {}
    print(f"📂 총 {len(file_list)}개 파일 읽기 시작...")

    for file in file_list:
        try:
            basename = os.path.basename(file)
            parts = basename.split('_')
            etf_name = f"{parts[0]}_{parts[1]}"
            df = pd.read_csv(file)
            df['날짜'] = pd.to_datetime(df['날짜'])
            df.set_index('날짜', inplace=True)
            col_name = '종가' if '종가' in df.columns else df.columns[0]
            price_data[etf_name] = df[col_name]
        except Exception as e:
            continue

    # [수정] 데이터프레임 생성 및 열 이름 정렬
    full_df = pd.DataFrame(price_data).sort_index()
    full_df = full_df.reindex(sorted(full_df.columns), axis=1) # 컬럼 순서 고정
    full_df = full_df.ffill()
    returns_df = full_df.pct_change()

    rebalance_dates = pd.date_range(start=full_df.index.min(), end=full_df.index.max(), freq=freq)
    final_cluster_features = []

    for i in range(1, len(rebalance_dates)):
        end_date = rebalance_dates[i]
        lookback_start = end_date - pd.Timedelta(days=365)
        train_data = returns_df.loc[lookback_start:end_date].dropna(axis=1)

        if train_data.empty or train_data.shape[1] < n_clusters:
            continue

        scaler = StandardScaler()
        scaled_train = scaler.fit_transform(train_data.T)

        # random_state 고정
        kmeans = KMeans(n_clusters=n_clusters, random_state=42, n_init=10)
        cluster_labels = kmeans.fit_predict(scaled_train)

        # --- [핵심 추가: 군집 번호 정렬 로직] ---
        # 군집별 평균 수익률을 계산하여 수익률이 낮은 순서대로 0, 1, 2번을 다시 부여합니다.
        cluster_info = pd.DataFrame({'label': cluster_labels, 'ret': train_data.mean()})
        # 군집별 평균 수익률 순위 계산
        rank_map = cluster_info.groupby('label')['ret'].mean().sort_values().index
        # 기존 라벨을 수익률 순위 라벨로 변경
        new_label_map = {old: new for new, old in enumerate(rank_map)}
        fixed_labels = np.array([new_label_map[label] for label in cluster_labels])

        cluster_map = pd.Series(fixed_labels, index=train_data.columns)
        # ------------------------------------------

        next_end = rebalance_dates[i+1] if i+1 < len(rebalance_dates) else full_df.index.max()
        target_returns = returns_df.loc[end_date:next_end]
        period_features = pd.DataFrame(index=target_returns.index)

        for c in range(n_clusters):
            members = cluster_map[cluster_map == c].index
            active_returns = target_returns[members]
            period_features[f'C{c}_Ret'] = active_returns.mean(axis=1)
            period_features[f'C{c}_Disp'] = active_returns.std(axis=1)
            period_features[f'C{c}_Vol'] = period_features[f'C{c}_Ret'].rolling(window=20, min_periods=5).std()
            period_features[f'C{c}_Count'] = active_returns.notna().sum(axis=1)

        final_cluster_features.append(period_features)
        print(f"📡 {end_date.date()} 기준 군집화 완료 (수익률 순 정렬 적용)")

    if final_cluster_features:
        final_df = pd.concat(final_cluster_features).sort_index()
        return final_df[~final_df.index.duplicated(keep='first')]
    return None

