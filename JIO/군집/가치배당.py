    fig, axes = plt.subplots(3, 1, figsize=(15, 20))

    # --- 1. 군집별 누적 수익률 ---
    return_cols = [c for c in final_df.columns if '_Ret' in c]
    # 군집 번호순(C0, C1, C2)으로 정렬하여 출력
    return_cols.sort()
