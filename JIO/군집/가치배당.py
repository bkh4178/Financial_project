import seaborn as sns
import matplotlib.pyplot as plt
import matplotlib.font_manager as fm
import os
import pandas as pd
import numpy as np
from sklearn.preprocessing import StandardScaler
from sklearn.cluster import KMeans

def visualize_cluster_membership(file_list, n_clusters=3, freq='Q'):
    # --- [데이터 처리 로직] ---
    price_data = {}
    for file in file_list:
        try:
            basename = os.path.basename(file)
            etf_name = f"{basename.split('_')[0]}_{basename.split('_')[1]}"
            df = pd.read_csv(file, encoding='utf-8-sig')

            df.iloc[:, 0] = pd.to_datetime(df.iloc[:, 0], errors='coerce')
            df = df.dropna(subset=[df.columns[0]])
            df.set_index(df.columns[0], inplace=True)

            price_data[etf_name] = df.iloc[:, 0]
        except: continue

    # 1. 데이터프레임 생성 및 '열 이름 순서' 고정
    full_df = pd.DataFrame(price_data).sort_index()
    full_df = full_df.reindex(sorted(full_df.columns), axis=1) # 컬럼 순서 정렬
    full_df = full_df.ffill()
    returns_df = full_df.pct_change()

    rebalance_dates = pd.date_range(start=full_df.index.min(), end=full_df.index.max(), freq=freq)

    membership_history = []

    for i in range(1, len(rebalance_dates)):
        end_date = rebalance_dates[i]
        lookback_start = end_date - pd.Timedelta(days=365)
        train_data = returns_df.loc[lookback_start:end_date].dropna(axis=1)

        if train_data.empty or train_data.shape[1] < n_clusters: continue

        scaler = StandardScaler()
        scaled_train = scaler.fit_transform(train_data.T)

        # random_state 고정
        kmeans = KMeans(n_clusters=n_clusters, random_state=42, n_init=10)
        cluster_labels = kmeans.fit_predict(scaled_train)

        # --- [핵심 추가: 군집 번호를 수익률 순서대로 재매핑] ---
        # 이 로직이 있어야 돌릴 때마다 번호(색상)가 바뀌지 않습니다.
        cluster_info = pd.DataFrame({'label': cluster_labels, 'ret': train_data.mean()})
        # 수익률 낮은 군집부터 0, 1, 2 순서 부여
        rank_map = cluster_info.groupby('label')['ret'].mean().sort_values().index
        new_label_map = {old: new for new, old in enumerate(rank_map)}
        fixed_labels = [new_label_map[label] for label in cluster_labels]
        # ---------------------------------------------------

        period_membership = pd.Series(fixed_labels, index=train_data.columns, name=end_date.date())
        membership_history.append(period_membership)

    # 데이터프레임 통합 (행: ETF, 열: 날짜) 및 종목 이름순 정렬
    membership_df = pd.concat(membership_history, axis=1).sort_index()

    # --- [시각화 설정] ---
    sns.set_style("white")

    # 코랩/맥 환경에 따른 폰트 경로 자동 설정
    font_path = '/usr/share/fonts/truetype/nanum/NanumBarunGothic.ttf'
    if not os.path.exists(font_path): # 맥 환경일 경우
        font_path = '/System/Library/Fonts/Supplemental/AppleGothic.ttf'

    font_prop = fm.FontProperties(fname=font_path)
    plt.rc('font', family=font_prop.get_name())
    plt.rcParams['axes.unicode_minus'] = False

    plt.figure(figsize=(22, 12))
    # cmap='viridis' (0: 보라, 1: 초록, 2: 노랑)
    sns.heatmap(membership_df, annot=True, cmap='viridis', cbar=True, linewidths=.5)

    plt.title('ETF 군집 멤버십 변화 (수익률 기반 라벨 고정: 0=Low, 2=High)', fontsize=20, fontweight='bold', pad=25)
    plt.xlabel('리밸런싱 시점 (Rebalance Date)', fontsize=14, labelpad=15)
    plt.ylabel('ETF 명칭 (알파벳순)', fontsize=14, labelpad=15)
    plt.xticks(rotation=45)

    plt.tight_layout()
    plt.show()

